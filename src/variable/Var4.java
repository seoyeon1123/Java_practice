package variable;

public class Var4 {
    public static void main(String[] args) {
        int a; // 변수를 선언한 것
        a= 1; // 변수를 초기화 한 것
        System.out.println(a); //출력한 것


        int b = 2; // 변수 선언과 초기화를 한번에
        System.out.println(b);

        int c = 3, d = 4; // 여러 변수 선언과 초기화를 한번에
        System.out.println(c);
        System.out.println(d);
    }
}
